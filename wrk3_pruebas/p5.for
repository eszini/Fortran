

        PROGRAM SumaDosNumeros
                REAL A, B, SUMA

                WRITE(*,*) 'Ingrese el primer número:'
                READ(*,*) A

                WRITE(*,*) 'Ingrese el segundo número:'
                READ(*,*) B

                SUMA = A + B

                WRITE(*,*) 'La suma de ', A, ' y ', B, ' es ', SUMA 
                
        END
